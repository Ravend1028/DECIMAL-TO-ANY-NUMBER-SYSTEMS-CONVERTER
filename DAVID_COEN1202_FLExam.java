    public String toAnyNumberSystems(int num, int base){
        StringBuilder sb = new StringBuilder();
        String NumberSystemCode = "0123456789ABCDEF";
        String binNumber = "";
        int binDigit;
        
        if (num > 0){
            binNumber = toAnyNumberSystems(num/base, base);
            binDigit = num % base;
            sb.append(binNumber + NumberSystemCode.charAt(binDigit));
        }
        return sb.toString();
    }
