    private void jbtnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConvertActionPerformed
      
        if(jtxtDec.getText().isEmpty() == true){
            jbtnConvert.setEnabled(false);
            jbtnAddToList.setEnabled(false);
            jbtnReset.setEnabled(false);
            JOptionPane.showMessageDialog(null, "You Must Enter A Number", "Decimal Converter", JOptionPane.INFORMATION_MESSAGE);
        }else{
            jbtnConvert.setEnabled(true);
            jbtnAddToList.setEnabled(true);
            jbtnReset.setEnabled(true);
            int Decimal = Integer.parseInt(jtxtDec.getText());
            jtxtDec.setText(String.valueOf(Decimal));
        }

        if(jtxtDec.getText().isEmpty() == false){
            int Decimal = Integer.parseInt(jtxtDec.getText());
            if(Decimal < 0){
                JOptionPane.showMessageDialog(null, "Enter A Non Negative Integer.", "Decimal Converter", JOptionPane.INFORMATION_MESSAGE);
            }else{
                jtxtDec.setText(String.valueOf(Decimal));
                jbtnConvert.setEnabled(true);
                jbtnAddToList.setEnabled(true);
                jbtnReset.setEnabled(true);
            }
        }else {
            int Decimal = Integer.parseInt(jtxtDec.getText());
            jtxtDec.setText(String.valueOf(Decimal));
            jbtnConvert.setEnabled(false);
            jbtnAddToList.setEnabled(false);
            jbtnReset.setEnabled(false);
        }
        int Decimal = Integer.parseInt(jtxtDec.getText());
        String Number = jcmbBase.getSelectedItem().toString();
        int Num = Integer.parseInt(Number);
        jlblEquiv.setText(toAnyNumberSystems(Decimal,Num));
        jbtnConvert.setEnabled(false);
        jbtnAddToList.setEnabled(true);
    }//GEN-LAST:event_jbtnConvertActionPerformed

